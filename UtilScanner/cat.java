public class cat {

    private String name;
    private int age;

    //constructor

    public cat(){
        this.name = "unknown";
        this.age = 0;
    }

    //getters and setters

    public String getName() {
        return name;
    }

    public void setName() {
        this.name = name;
    }
    
    public int getAge(){
        return age;
    }

    public void setAge() {
        this.age = age;
    }
}
